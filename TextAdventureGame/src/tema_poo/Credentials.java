package tema_poo;

public class Credentials {
    private String email = "";
    private String password = "";

    void setEmail(String email) {
        this.email = email;
    }

    void setPassword(String password) {
        this.password = password;
    }

    String getEmail() {
        return email;
    }

    String getPassword() {
        return password;
    }
}
